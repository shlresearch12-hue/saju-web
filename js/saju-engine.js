    function getNumElement(n) {
        const d = n % 10;
        if (d === 1 || d === 2) return '목';
        if (d === 3 || d === 4) return '화';
        if (d === 5 || d === 6) return '토';
        if (d === 7 || d === 8) return '금';
        return '수'; // 9, 0
    }

